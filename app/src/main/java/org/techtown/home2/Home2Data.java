package org.techtown.home2;

public class Home2Data {
    private String name;
    private String price;
    private int resId;

    public Home2Data(int res, String name, String price){
        this.resId = res;
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public int getResId() {
        return resId;
    }

    public void setResId(int resId) {
        this.resId = resId;
    }
}
